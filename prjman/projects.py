from .const import DEFAULT_EDITOR_FILE_PATH, PROJECTS_FILE_PATH


def read_projects_file():
    with open(PROJECTS_FILE_PATH, "rb") as f:
        projects = pickle.load(f)
    return projects

def find_all_projects():
    import os
    from . import Project
    projects = []

    all_projects = {x: [os.path.join(x, y) for y in os.listdir(x) if os.path.isdir(os.path.join(x, y))] for x in [os.path.join('/home/diverso/p/', i) for i in ('gitclones', 'OnGit', 'NotOnGit')]}

    for prj_category in all_projects:
        for p in all_projects[prj_category]:
            projects.append(Project(p.split('/')[-1], p, prj_category.split('/')[-1].lower()))
    return projects


def write_to_projects_file(new_projects):
    with open(PROJECTS_FILE_PATH, "wb") as f:
        pickle.dump(new_projects, f)


def find_project_by_name(project_name):
    return tuple([x for x in read_projects_file() if x.name == project_name])


def get_default_editor():
    with open(DEFAULT_EDITOR_FILE_PATH, 'r') as f:
        s = f.read().strip()
    return s


def set_default_editor(new_editor):
    with open(DEFAULT_EDITOR_FILE_PATH, 'w') as f:
        f.write(new_editor)

